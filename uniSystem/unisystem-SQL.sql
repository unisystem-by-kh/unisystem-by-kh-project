-- 회원 초기 데이터 테스트용
CREATE TABLE MEMBER (
    MEMBER_NO VARCHAR2(11) NOT NULL,
    MEMBER_PW VARCHAR2(50),
    MEMBER_ADDR VARCHAR2(150),
    MEMBER_EMAIL VARCHAR2(50),
    MEMBER_PHONE CHAR(11),
    MEMBER_GEN CHAR(1),
    MEMBER_DATE DATE DEFAULT SYSDATE NOT NULL,
    MEMBER_GRADE NUMBER,
    MEMBER_TERM NUMBER,
    MEMBER_STATUS CHAR(1) DEFAULT 'N' NOT NULL,
    MEMBER_NAME VARCHAR2(20) NOT NULL,
    DEPARTMENT_NO NUMBER NOT NULL,
    CONSTRAINT PK_MEMBER PRIMARY KEY (MEMBER_NO)
);

CREATE TABLE DEPARTMENT (
    DEPARTMENT_NO NUMBER NOT NULL,
    DEPARTMENT_NAME VARCHAR2(30) NOT NULL,
    DEPARTMENT_PRICE NUMBER DEFAULT 100 NOT NULL,
    CONSTRAINT PK_DEPARTMENT PRIMARY KEY (DEPARTMENT_NO)
);

